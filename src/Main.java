public class Main {
    public static void main(String[] args) {
        int a = 10, b = 3;

        a += 5; // 복합대입 연산자, a = a + 5;

        System.out.println(a);

        b -= 1;
        System.out.println(b);
        b *= 3;
        System.out.println(b);
        b /= 2;
        System.out.println(b);
        b %= 2;
        System.out.println(b);


    }
}
